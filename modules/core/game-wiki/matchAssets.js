// Deadlock rank badge = tier*10 + subrank (1–6); a scrim with no rank reports 0 → "Unranked".
export function rankFromBadge(badge) {
  const b = Number(badge);
  if (!Number.isFinite(b) || b <= 0) return { name: 'Unranked', color: 'var(--text-muted)', tier: 0, sub: 0 };
  const tier = Math.floor(b / 10);
  const sub = b % 10;
  const r = RANK_NAMES[tier];
  return { name: r ? `${r.n}${sub ? ` ${sub}` : ''}` : `Tier ${tier}`, color: r ? r.c : 'var(--text-muted)', tier, sub };
}

